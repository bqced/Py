ss = 'Sammy Shark!'
print(ss[4])
#this returns the letter at index 4
print(ss[6:10])
#prints string from index 6 to 10
print(ss[:5])
#prints string from index 0 to 5
print(ss[7:])
#prints string from index 7 till the end
print(ss[-4:-1])
#prints string from index -4 or 8 till the end
print(ss[6:11:1])
#prints string from index 6 to 10
print(ss[::-1])
#prints string backwards
print(ss[::-2])
#prints string backwards but steps in 2