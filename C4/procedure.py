# Define a function called maxof2 that takes two parameters, a and b
def maxof2(a, b):
# Check if a is greater than b
    if a > b:
# If a is greater, print the value of a
        print(a)
    else:
# If b is greater or equal, print the value of b
        print(b)
# Call the maxof2 function with arguments 3 and 6
maxof2(3, 6)